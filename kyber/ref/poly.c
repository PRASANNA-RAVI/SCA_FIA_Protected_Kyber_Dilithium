static int shift_lfsr(unsigned int *lfsr, unsigned int polynomial_mask)
{
    int feedback;

    feedback = *lfsr & 1;
    *lfsr >>= 1;
    if(feedback == 1)
        *lfsr ^= polynomial_mask;
    return *lfsr;
}

static int get_random(void)
{
    int temp;
    unsigned int POLY_MASK_HERE_1 = 0xABCD5162;
    unsigned int POLY_MASK_HERE_2 = 0x8652ACFE;
    static unsigned int lfsr_1 = 0x55AAEEFF;
    static unsigned int lfsr_2 = 0xFFAA8844;
    shift_lfsr(&lfsr_1, POLY_MASK_HERE_1);
    shift_lfsr(&lfsr_2, POLY_MASK_HERE_2);
    temp = (shift_lfsr(&lfsr_1, POLY_MASK_HERE_1) ^ shift_lfsr(&lfsr_2, POLY_MASK_HERE_2)) & 0XFF;
    return (temp);
}

static unsigned char random_buffer[RANDOM_BUFFER_SIZE_NEW];
static unsigned char seed[32];

// A function to generate a random
// permutation of arr[]
static void randomize (unsigned char arr[], int n)
{
  unsigned char temp;
  int total_rand_bytes_remaining = 0;

    // Start from the last element and swap
    // one by one. We don't need to run for
    // the first element that's why i > 0
    for (int i = n - 1; i > 0; i--)
    {
        if(total_rand_bytes_remaining <= 0)
        {
          // randombytes(seed,32);
          // prf(random_buffer, RANDOM_BUFFER_SIZE_NEW, seed, 0);

          for(int lp = 0; lp < RANDOM_BUFFER_SIZE_NEW; lp++)
            random_buffer[lp] = get_random();

          total_rand_bytes_remaining = RANDOM_BUFFER_SIZE_NEW;
        }
        temp = random_buffer[RANDOM_BUFFER_SIZE_NEW-total_rand_bytes_remaining];
        total_rand_bytes_remaining = total_rand_bytes_remaining-1;

        // Pick a random index from 0 to i
        int j = temp % (i + 1);

        // Swap arr[i] with the element
        // at random index

        temp = arr[i];
        arr[i] = arr[j];
        arr[j] = temp;
    }
}

